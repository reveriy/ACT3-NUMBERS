const difference = firstFavNumber - secondFavNumber;
console.log("21 - 16 =", difference);
 
console.log("Multiplication")
const product = firstFavNumber * secondFavNumber;
console.log("21 * 16 =", product);
 
console.log("Division")
const quotient = firstFavNumber / secondFavNumber;
console.log("21 / 16 =", quotient);

console.log("Modulus")
const mod = firstFavNumber % secondFavNumber;
console.log("21 % 16 =", mod);

console.log("Exponentiation")
const power = firstFavNumber ** secondFavNumber;
console.log("21 % 16 =", power);
